# 集合的运算：
s = {1, 2, 3, 4, 5}
t = {3, 4, 5, 6, 7}
#  & 求交集
r = s & t
print(r)  # {3, 4, 5}
#  | 求并集
r = s | t
print(r)  # {1, 2, 3, 4, 5, 6, 7}
#  - 求差集
r = s - t
print(r) # {1, 2}

# ^求异或集（不相交的部分）
r=s^t
print(r)  # {1, 2, 6, 7}
