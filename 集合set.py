# 集合set
# 集合和列表相似
# 不同：集合只能存储不可变对象，集合存储对象无序，集合存放元素不能重复
s = set()  # 使用set创建空集合
print(s)
d = {'a': 1, 'b': 2}
s = set(d)  # 将字典转化为集合
print(s)  # {'a', 'b'}

